// There are diffent kinds of operators:


//[1] Arithmetic Operators: These operators perform mathematical operations.

// Addition (+)
// Subtraction (-)
// Multiplication (*)
// Division (/)
// Modulus (%) - Returns the remainder of a division.
// Increment (++) - Increases the value of a variable by 1.
// Decrement (--) - Decreases the value of a variable by 1.


// Example:
const a = 1 + 2;
console.log(a); // addition

const b = 4 - 4;
console.log(b); // subtraction

const c = 10 % 2;
console.log('The reminder is : ',c); // Modulus (%)

let d = 10 // increment
console.log('Its increments but printed the actual value : ',d++); // output 10 [it increments to 11 ,but prints the actual value 10]
console.log('Its increments but printed the actual value :',d++); // output 11 [now it prints the 11 and again increments to 12 but not prints the actual value 11 not 12.]

// decrement
let e = 20;
console.log('Its decrements but printed the actual value :',e--);
console.log('Its decrements but printed the actual value :',e--);


